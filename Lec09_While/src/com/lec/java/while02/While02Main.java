package com.lec.java.while02;

public class While02Main {

	public static void main(String[] args) {
		System.out.println("while 연습");
		
		// 구구단 2단
		int i = 1;
		while(i <= 9) {
			System.out.printf("2 x " + i + " = " + "%2d\n", (2 * i));
			i++;
		} // end while
		
	} // end main()

} // end class While02Main









